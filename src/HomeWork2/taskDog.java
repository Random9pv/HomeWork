package HomeWork2;

public class taskDog {
    private String years;
    private String breed;

    taskDog(String years, String breed) {
        this.years = years;
        this.breed = breed;
    }

    public String getYears() {
        return years;
    }

    public String getBreed() {
        return breed;
    }

    public void setYears(String years) {
        this.years = years;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }

}
